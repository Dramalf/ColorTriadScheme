let res={};
let style=''
res.labList=[];
a.forEach((l,index)=>{
    console.log(index)
    if(typeof l[0]==='string'){
        style=l.shift();
        // console.log(style,'style')
        res[style]=[]
    }
    // console.log(l)
    const [o,s2,s3]=l;
    let o1=o.map(c=>`#${c}`)
    let o2=s2.toString().split('').map(i=>{
        return o1[i-1]
    })
    let o3=s3.toString().split('').map(i=>{
        return o1[i-1]
    })
    const hex=[o1,o2,o3];
    // console.log(hex)
    const rgb=hex.map(o=>o.map(hexToRGB))
    let lab=hex.map(o=>o.map(c=>{
        // console.log(c)
        
        return labFromArgb(argbFromHex(c))
    }))
    res.labList.push(...lab);
    res[style].push({hex,rgb,lab})
    
})

// console.log('%o',res)
function hexToRGB(hex) {
    // 去除颜色字符串中的井号
    const cleanHex = hex.replace('#', '');
  
    // 将颜色字符串拆分为红、绿、蓝三个部分
    const red = parseInt(cleanHex.substring(0, 2), 16);
    const green = parseInt(cleanHex.substring(2, 4), 16);
    const blue = parseInt(cleanHex.substring(4, 6), 16);
  
    // 返回RGB格式的颜色值
    return [ red, green, blue ];
  }
